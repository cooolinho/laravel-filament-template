#!/bin/bash

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Create .env file                              ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
cp .env.example .env

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Installing vendor/                            ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
composer install --ignore-platform-reqs

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Installing node_modules/                      ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
yarn install

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Create APP_KEY 'php artisan key:generate'     ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
php artisan key:generate

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Run migrations                                ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
php artisan migrate

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Install Filament assets                       ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
php artisan filament:assets

# -------------------------------------------------------------------------------- #

echo "╔════════════════════════════════════════════════════╗
║                                                    ║
║  ️ ➡️ Building assets                               ║
║                                                    ║
╚════════════════════════════════════════════════════╝"
yarn build
